#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	d;

	d = (unsigned char)c;
	if ((c < 0 || c > 255))
		return (0);
	return ((d <= 'Z' && d >= 'A') || (d <= 'z' && d >= 'a'));
}
