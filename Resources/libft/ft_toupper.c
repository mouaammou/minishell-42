#include "libft.h"

int	ft_toupper(int c)
{
	unsigned char	d;

	d = (unsigned char)c;
	if (c > 127 || c < 0)
		return (c);
	if (d <= 'z' && d >= 'a')
		d += 'A' - 'a';
	return (d);
}
