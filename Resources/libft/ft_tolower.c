#include "libft.h"

int	ft_tolower(int c)
{
	unsigned char	d;

	d = (unsigned char)c;
	if (c > 127 || c < 0)
		return (c);
	if (d <= 'Z' && d >= 'A')
		d += 'a' - 'A';
	return (d);
}
